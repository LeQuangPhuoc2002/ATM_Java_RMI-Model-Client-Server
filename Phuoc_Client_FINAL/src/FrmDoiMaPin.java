    private void mpcKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mpcKeyPressed
        // TODO add your handling code here:
        String matkhaucu = String.valueOf(mpc.getPassword());
        int lengthMatKhau = matkhaucu.length();
        char c = evt.getKeyChar();
        if (Character.isDigit(c)) {
            if (lengthMatKhau < 6) {
                mpc.setEditable(true);
            } else {
                mpc.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACKSPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                mpc.setEditable(true);
            } else {
                mpc.setEditable(false);
            }
        }
    }//GEN-LAST:event_mpcKeyPressed

