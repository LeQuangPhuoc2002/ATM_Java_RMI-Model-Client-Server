    public void run() {
        try {
            ITinhToan server = ServerUtility.getServerInstance();
            String aa = mpm.getText();
            String bb = xnmp.getText();
            String cc = mpc.getText();

            if (aa.isEmpty() || bb.isEmpty() || cc.isEmpty()) {
                txtloi.setText("Vui lòng nhập đủ các trường");
            } else if (aa.length() != 6 || bb.length() != 6 || cc.length() != 6) {
                txtloi.setText("Mã PIN phải là 6 chữ số");
                mpm.setText("");
                xnmp.setText("");
                mpc.setText("");
            } else {
                try {
                    long aaa = Long.parseLong(aa);
                    long bbb = Long.parseLong(bb);
                    long ccc = Long.parseLong(cc);
                    System.out.println(SharedVariables.stk);
                    System.out.println(ccc);
                    System.out.println(server.KTDangNhap(SharedVariables.stk, ccc));
                    if (server.DangNhap(SharedVariables.stk, ccc)) { // kiểm tra mã pin cũ đúng hay sai
                        if (aaa == ccc) {
                            txtloi.setText("Trùng mã PIN cũ và mới");
                            mpm.setText("");
                            xnmp.setText("");
                            mpc.setText("");
                            return;
                        } else {
                            if (aaa != bbb) {
                                txtloi.setText("Xác nhận mã PIN không trùng khớp");
                                mpm.setText("");
                                xnmp.setText("");
                            } else {
                                server.DoiMatKhau(SharedVariables.stk, ccc, bbb);
                                txtloi.setText("Thay đổi mã PIN đã thành công.");
                                mpm.setText("");
                                xnmp.setText("");
                                mpc.setText("");
                            }
                        }
                    } else {
                        txtloi.setText("Sai mã PIN cũ , mời nhập lại");
                        mpm.setText("");
                        xnmp.setText("");
                        mpc.setText("");
                    }
                } catch (NumberFormatException e) {
                    txtloi.setText("Mã PIN phải là số");
                    mpm.setText("");
                    xnmp.setText("");
                    mpc.setText("");
                }
            }
        } catch (Exception tt) {
            System.out.print(tt);
        }
    }

