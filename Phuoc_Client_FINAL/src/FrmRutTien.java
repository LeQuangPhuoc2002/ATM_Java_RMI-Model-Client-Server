        int lengthMatKhau = matkhau.length();
        char c = evt.getKeyChar();

        if (Character.isDigit(c)) {
            if (lengthMatKhau < 10) {
                // Nếu là số và độ dài chuỗi dưới 11, cho phép thêm vào
            } else {
                // Nếu độ dài đã đạt 11, không cho phép thêm vào
                evt.consume(); // Ngăn chặn ký tự được hiển thị
            }
        } else if (evt.getExtendedKeyCode() != KeyEvent.VK_BACKSPACE && evt.getExtendedKeyCode() != KeyEvent.VK_DELETE) {
            // Ngăn chặn các ký tự không phải là số và không phải là ký tự xóa
            evt.consume();
        }

        // Định dạng số và cập nhật JTextField
        DecimalFormat decimalFormat = new DecimalFormat("#,##0");
        try {
            // Chỉ thực hiện chuyển đổi nếu chuỗi không rỗng
            if (!matkhau.isEmpty()) {
                long number = Long.parseLong(matkhau.replace(",", ""));
                str.setText(decimalFormat.format(number));
                System.out.println("Số sau khi định dạng: " + decimalFormat.format(number));
            }
        } catch (NumberFormatException e) {
            // Xử lý lỗi nếu chuỗi không thể chuyển đổi thành số
            System.err.println("Lỗi NumberFormatException: " + e.getMessage());
        }
