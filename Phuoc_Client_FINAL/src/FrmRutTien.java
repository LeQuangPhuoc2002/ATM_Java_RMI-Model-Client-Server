    public void run() {
        try {
            ITinhToan server = ServerUtility.getServerInstance();
            String a = str.getText().replaceAll(",", "");
            selectedTextField = null;

            if (a.isEmpty()) {
                txtloi.setText("Vui lòng nhập số tiền");
                return;
            } else {
                try {
                    long aa = Long.parseLong(a);

                    // Xử lý rút tiền
                    int p = 0;
                    p = server.RutTien(SharedVariables.stk, aa);
                    if (p == 1) {

                        str.setText("");
                        this.dispose();//hủy Frame: FrmDangNhap
                        FrmConfirm f = new FrmConfirm();
                        f.setVisible(true);

                    } else if (p == -1) {
                        txtloi.setText("Có lỗi trong cơ sở dữ liệu");
                        str.setText("");
                    } else if (p == -2) {
                        txtloi.setText("Lỗi không ghi được lịch sử giao dịch");
                        str.setText("");
                    } else if (p == -3) {
                        txtloi.setText("Số dư không đủ để thực hiện giao dịch, duy trì mức tối thiểu 50,000");
                        str.setText("");
                    } else if (p == -4) {
                        txtloi.setText("Rút bội số của 50.000 ít nhất 50.000 tối đa 10.000.000");
                        str.setText("");
                    } else if (p == -5) {
                        txtloi.setText("Lỗi bạn rút quá giới hạn 20.000.000 một ngày");
                        str.setText("");
                    }

                } catch (NumberFormatException e) {
                    txtloi.setText("Tiền phải là một số");
                    str.setText("");
                }
            }
        } catch (Exception tt) {
            tt.printStackTrace();
        }
    }

